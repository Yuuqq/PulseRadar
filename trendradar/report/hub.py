_HUB_JS = """
let currentMode = 'all';
let searchTerm = '';
let debounceTimer;

function onSearch(value) {
  clearTimeout(debounceTimer);
  debounceTimer = setTimeout(function() {
    searchTerm = value.toLowerCase().trim();
    applyFilters();
  }, 200);
}

function filterMode(mode) {
  currentMode = mode;
  document.querySelectorAll('.mode-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.mode === mode);
  });
  applyFilters();
}

function applyFilters() {
  document.querySelectorAll('.date-group').forEach(function(group) {
    var cards = group.querySelectorAll('.card');
    var anyVisible = false;
    cards.forEach(function(card) {
      var matchesMode = currentMode === 'all' || card.dataset.mode === currentMode;
      var text = (card.dataset.date + ' ' + card.textContent).toLowerCase();
      var matchesSearch = !searchTerm || text.indexOf(searchTerm) !== -1;
      var visible = matchesMode && matchesSearch;
      card.style.display = visible ? '' : 'none';
      if (visible) anyVisible = true;
    });
    group.style.display = anyVisible ? '' : 'none';
  });
}

function toggleTheme() {
  var body = document.body;
  var next = body.getAttribute('data-theme') === 'dark' ? '' : 'dark';
  body.setAttribute('data-theme', next);
  try { localStorage.setItem('hub-theme', next); } catch(e) {}
}

function applyLang(lang) {
  document.body.dataset.lang = lang;
  document.documentElement.setAttribute('lang', lang === 'zh' ? 'zh-CN' : 'en');
  var btn = document.getElementById('lang-toggle');
  if (btn) btn.textContent = lang === 'zh' ? 'EN' : '中';
  document.querySelectorAll('[data-i18n-en]').forEach(function(el) {
    var v = el.getAttribute(lang === 'zh' ? 'data-i18n-zh' : 'data-i18n-en');
    if (v != null) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-en-placeholder]').forEach(function(el) {
    var v = el.getAttribute(lang === 'zh' ? 'data-i18n-zh-placeholder' : 'data-i18n-en-placeholder');
    if (v != null) el.placeholder = v;
  });
  try { localStorage.setItem('hub-lang', lang); } catch(e) {}
}
function toggleLang() {
  var current = document.body.dataset.lang === 'zh' ? 'zh' : 'en';
  applyLang(current === 'zh' ? 'en' : 'zh');
}

function setMastheadDate() {
  var el = document.getElementById('masthead-date');
  if (!el) return;
  var reports = (window.__MANIFEST__ && window.__MANIFEST__.reports) || [];
  var latest = reports[0];
  for (var i = 0; i < reports.length; i++) {
    if (!latest || (reports[i].date || '') > (latest.date || '')) latest = reports[i];
  }
  if (latest && latest.date) {
    el.textContent = latest.date;
  } else {
    var d = new Date();
    el.textContent = d.toISOString().slice(0, 10);
  }
}

(function() {
  try {
    var savedTheme = localStorage.getItem('hub-theme');
    if (savedTheme) document.body.setAttribute('data-theme', savedTheme);
  } catch(e) {}
  try {
    var savedLang = localStorage.getItem('hub-lang');
    applyLang(savedLang === 'zh' ? 'zh' : 'en');
  } catch(e) { applyLang('en'); }
  setMastheadDate();
})();
"""
