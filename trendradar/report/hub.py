def _build_latest_links(latest_by_mode: dict[str, dict]) -> str:
    if not latest_by_mode:
        return ""
    mode_labels_en = {"current": "Current", "daily": "Daily", "incremental": "Incremental"}
    mode_labels_zh = {"current": "当前榜单", "daily": "全天汇总", "incremental": "增量分析"}
    links = []
    for mode, report in sorted(latest_by_mode.items()):
        label_en = mode_labels_en.get(mode, mode)
        label_zh = mode_labels_zh.get(mode, mode)
        path = report.get("path", "#")
        links.append(
            f'<a href="{path}" class="latest-link" data-i18n-en="{label_en}" data-i18n-zh="{label_zh}">{label_en}</a>'
        )
    return (
        '<div class="latest-bar">'
        '<span class="latest-label" data-i18n-en="LATEST" data-i18n-zh="最新报告">LATEST</span>'
        f'{"".join(links)}'
        "</div>"
    )


_MODE_LABELS_EN = {"current": "Current", "daily": "Daily", "incremental": "Incremental"}
_MODE_LABELS_ZH = {"current": "当前榜单", "daily": "全天汇总", "incremental": "增量分析"}


def _build_cards_html(grouped: dict[str, list[dict]]) -> str:
    sections = []
    for date in sorted(grouped.keys(), reverse=True):
        reports = grouped[date]
        cards = []
        for r in reports:
            mode = r.get("mode", "")
            label_en = _MODE_LABELS_EN.get(mode, r.get("mode_label", mode))
            label_zh = _MODE_LABELS_ZH.get(mode, r.get("mode_label", mode))
            path = r.get("path", "#")
            time_display = r.get("time", "")
            total = r.get("total_titles", 0)
            stats = r.get("stats_count", 0)
            cards.append(
                f'<a href="{path}" class="card" data-mode="{mode}" data-date="{date}">'
                f'<div class="card-time">{time_display}</div>'
                f'<span class="card-badge mode-{mode}" data-i18n-en="{label_en}" data-i18n-zh="{label_zh}">{label_en}</span>'
                f'<div class="card-stats">'
                f'<span class="stat-num">{total}</span>'
                f'<span class="stat-label" data-i18n-en="signals" data-i18n-zh="条新闻">signals</span>'
                f'<span class="stat-sep">·</span>'
                f'<span class="stat-num">{stats}</span>'
                f'<span class="stat-label" data-i18n-en="topics" data-i18n-zh="个关键词">topics</span>'
                f"</div>"
                f"</a>"
            )
        sections.append(
            f'<section class="date-group" data-date="{date}">'
            f'<h2 class="date-heading">{date}</h2>'
            f'<div class="card-grid">{"".join(cards)}</div>'
            f"</section>"
        )
    if not sections:
        return '<p class="empty-state" data-i18n-en="No issues yet" data-i18n-zh="暂无报告">No issues yet</p>'
    return "\n".join(sections)


